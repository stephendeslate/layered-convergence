import { IsString, MaxLength, IsOptional } from 'class-validator';

export class UpdatePipelineDto {
  @IsOptional()
  @IsString()
  @MaxLength(200)
  name?: string;

  @IsOptional()
  @IsString()
  @MaxLength(2000)
  description?: string;

  @IsOptional()
  @IsString()
  @MaxLength(50)
  schedule?: string;
}
